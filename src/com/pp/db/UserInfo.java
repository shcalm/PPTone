package com.pp.db;

public class UserInfo {
	private String mName;
	private String mFansId;
	private String mFollowId;
	private String mUserIcon;
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public String getmFansId() {
		return mFansId;
	}
	public void setmFansId(String mFansId) {
		this.mFansId = mFansId;
	}
	public String getmFollowId() {
		return mFollowId;
	}
	public void setmFollowId(String mFollowId) {
		this.mFollowId = mFollowId;
	}
	public String getmUserIcon() {
		return mUserIcon;
	}
	public void setmUserIcon(String mUserIcon) {
		this.mUserIcon = mUserIcon;
	}
	
}
